const jwt = require('jsonwebtoken');
const User = require('../models/User');

const protect = async (req, res, next) => {
  let token;

  if (req.headers.authorization && req.headers.authorization.startsWith('Bearer')) {
    try {
      // Get token from header
      token = req.headers.authorization.split(' ')[1];

      // Verify token
      const decoded = jwt.verify(token, process.env.JWT_SECRET);

      // Get user from token
      req.user = await User.findById(decoded.id).select('-password');

      if (!req.user) {
        return res.status(401).json({ message: 'User not found' });
      }

      next();
    } catch (error) {
      console.error(error);
      return res.status(401).json({ message: 'Not authorized' });
    }
  }

  if (!token) {
    return res.status(401).json({ message: 'Not authorized, no token' });
  }
};

const authorize = (...roles) => {
  return (req, res, next) => {
    if (!req.user) {
      return res.status(401).json({ message: 'Not authorized' });
    }

    if (!roles.includes(req.user.role)) {
      return res.status(403).json({ 
        message: `User role ${req.user.role} is not authorized to access this route`
      });
    }
    next();
  };
};

const landlordOnly = async (req, res, next) => {
  if (req.user && req.user.role === 'landlord') {
    next();
  } else {
    res.status(403).json({ message: 'Access denied. Landlords only.' });
  }
};

const tenantOnly = async (req, res, next) => {
  if (req.user && req.user.role === 'tenant') {
    next();
  } else {
    res.status(403).json({ message: 'Access denied. Tenants only.' });
  }
};

module.exports = { protect, authorize, landlordOnly, tenantOnly };